const urlParams = new URLSearchParams(window.location.search);
const selectedRestaurant = urlParams.get('restaurant');

const restaurantData = jsonData[selectedRestaurant];

document.getElementById("restaurant-name").textContent = restaurantData.name;

document.getElementById("restaurant-description").textContent = restaurantData.description;



const menuItems = document.getElementById("menu-items");
restaurantData.menu.forEach(item => {
    const menuItem = document.createElement("li");
    menuItem.innerHTML = `
        <span class="dish-name">${item.name}</span>:
        <span class="price">${item.price}</span>
        <button class="add-to-cart-button" onclick = "addToCart('${item.name}', '${item.price}')" > Add to Cart</button >`;
    menuItems.appendChild(menuItem);
});

document.getElementById("restaurant-logo").src = `img/${selectedRestaurant.toLowerCase()}logo.png`;

document.getElementById("restaurant-location").textContent = restaurantData.location;

document.getElementById("restaurant-timings").textContent = restaurantData.timings;

document.getElementById("restaurant-ratings").innerHTML = `
    <p>Overall Rating: <span class="rating-value">${restaurantData.rating}</span>/5</p>
    <div class="stars">${generateStars(restaurantData.rating)}</div>`;

const socialMediaLinks = document.getElementById("social-media-links");
Object.entries(restaurantData.social_media).forEach(([platform, link]) => {
    const socialIcon = document.createElement("a");
    socialIcon.href = link;
    socialIcon.target = "_blank";
    socialIcon.innerHTML = `<img src="img/${platform}.png" alt="${platform}" class="small-icon">`;
    socialMediaLinks.appendChild(socialIcon);
});

function generateStars(rating) {
    const fullStars = Math.floor(rating);
    const emptyStars = 5 - fullStars;
    return "★".repeat(fullStars) + "☆".repeat(emptyStars);
}

function populateCarousel() {
    const carouselInner = document.querySelector(".carousel-inner");

    restaurantData.special_offers.forEach((offer, index) => {
        const carouselItem = document.createElement("div");
        carouselItem.classList.add("carousel-item");

        if (index === 0) {
            carouselItem.classList.add("active");
        }

        carouselItem.innerHTML = `
            <img src="img/${offer.image}" alt="${offer.name}" class="d-block w-100">
            <p class="special-offer">${offer.description}</p>
        `;

        carouselInner.appendChild(carouselItem);
    });
}

window.addEventListener("DOMContentLoaded", populateCarousel);

